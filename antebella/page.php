    Antepli bir çiftçi, yenilikçi bakış açısı ve fıstığa duyduğu aşkla yıllarca fıstık ticareti yaptı ve yüzlerce üreticiden aldığı fıstığı inceleyerek en mükemmel fıstığı aradı. 2008 yılında yatırımlarını hız kazandırarak Türkiye’nin en kaliteli antep fıstığı üreticisi olma yolunda önemli adımlar attı. Birkaç yıl sonra, kalite ve inovasyon odaklı atılan adımların karşılığı, Antepsan firmasını Türkiye’nin en büyük zincir marketlerinin; en büyük üreticisi konumuna taşıdı. Kendi bölgesinin insanını, çiftçisini ve ağaçlarını korumak, büyütmek ve kalkındırmak şiarıyla yapılan yepyeni yatırımlarla Antepsan, 2008’den bu yana %550 büyüme göstererek ülkemizin ve Antep sanayisinin göz bebeği konumuna geldi.

<p></p>Antepsan kendini yüksek kaliteli, sağlıklı ve en doğal ürünler sunmaya, tüketicilerin her gün daha iyi seçimler yapmalarına yardımcı olmaya adamış özel bir şirkettir. Kuruyemişler, sürülebilir ürünler, baklava ve pestil ürünleri dahil olmak üzere birçok çeşitte ürünü en kaliteli şekilde sunuyoruz. Antepsan’ın giderek büyüyen pazar ağı ve tedarik zinciri, dünyanın ve Türkiye’nin en büyük ağaç yemişleri yetiştiricisi, narenciye yetiştiricisi ve sürülebilir fıstık ezmesi grubunda öncü bir firma olma konumuna taşıyor.

<p></p>Antepsan ailesi olarak her zaman sağlıklı, kaliteli ve yenilikçi ürünlerle ilham veren bir marka olmaya, ülke ekonomimizi ve çiftçimizi en ileriye taşımaya devam ediyoruz.

